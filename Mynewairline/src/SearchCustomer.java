            con = DriverManager.getConnection("jdbc:mysql://localhost/mynewAirline","root","Shriganesh!123");
          pt = con.prepareStatement("Update customer set firstname = ?,lastname = ?,aadhar = ?,passport = ?,address = ?,dob = ?,gender = ?,contactno = ? where custid = ?"); 
          
        //
          pt.setString(1,firstname);
          pt.setString(2,lastname);
          pt.setString(3,aadhar);
          pt.setString(4,passport);
          pt.setString(5,address);
          pt.setString(6,date);
          pt.setString(7,gender);
