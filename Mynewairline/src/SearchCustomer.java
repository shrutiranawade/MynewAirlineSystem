            String aadhar = stxtaadhar.getText();
            String passport = stxtpassport.getText();
            String address = stxtaddress.getText();
            DateFormat da = new SimpleDateFormat("yyyy-MM-dd");
            
            String date = da.format(stxtdob.getDate());
            String gender;
            if(sr1.isSelected()){
                gender ="Male";
            }else{
                gender ="Female";}
            String contactno = stxtcontact.getText();
